//operadores
var numero1 = 10;
var numero2 = "20";
var numero3 = 30;

//mayor a
console.log(numero1 > numero2);
//menor a
console.log(numero1 < numero2);
//mayor o igual a
console.log(numero1 >= numero2);
//menor o igual a
console.log(numero1 <= numero2);
//igual a
console.log(numero1 == numero2);
//diferente de
console.log(numero1 != numero2);
//operador ternario
var resultado = numero1 > numero2 ? "numero1 es mayor que numero2" : "numero1 es menor que numero2";
console.log(resultado);
//operador logico
console.log(numero1 > numero2 && numero1 < numero3);
console.log(numero1 > numero2 || numero1 < numero3);
console.log(!(numero1 > numero2));

