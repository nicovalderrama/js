//Metodos para objetos
const persona = {
    nombre: 'Nicolas',
    apellido: 'Valderrama',
    edad: 20,
    fechaDeNacimiento:'30/06/2002',
};

//Object.keys() devuelve un array con las claves del objeto
console.log(Object.keys(persona));

//Object.values() devuelve un array con los valores del objeto
console.log(Object.values(persona));

//Object.entries() devuelve un array con las claves y valores del objeto
console.log(Object.entries(persona));

